function buildSetupScript(ideaText) {
  const projectName = slugify(ideaText) || 'my-app';

  return `#!/bin/bash
# Auto-generated by IdeaForge
# Idea: "${ideaText}"
# GitHub: ${GITHUB_USER}/${projectName} (personal)
# Run: bash setup.sh

set -e

GITHUB_USER="${GITHUB_USER}"
PROJECT="${projectName}"

echo ""
echo "⚡ IdeaForge Setup: $PROJECT"
echo "================================"

# 1. Create project folder
echo "→ Creating project folder..."
mkdir -p "$PROJECT"
cd "$PROJECT"

# 2. Python virtual environment
echo "→ Setting up Python virtual environment..."
python3 -m venv venv
source venv/bin/activate

# 3. Install base dependencies
echo "→ Installing FastAPI + Uvicorn..."
pip install --quiet fastapi==0.111.0 uvicorn==0.30.1

# 4. Write requirements.txt
cat > requirements.txt << 'REQEOF'
fastapi==0.111.0
uvicorn==0.30.1
REQEOF

# 5. Write .gitignore
cat > .gitignore << 'GITEOF'
__pycache__/
*.py[cod]
.env
venv/
*.db
*.sqlite
.DS_Store
GITEOF

# 6. Git init
echo "→ Initialising git..."
git init -b main
git add .
git commit -m "Phase 0 - Auto scaffold ✓" --quiet

# 7. Create GitHub repo + push
echo "→ Creating GitHub repo under $GITHUB_USER..."
gh repo create "$GITHUB_USER/$PROJECT" --public --source=. --remote=origin --push --quiet

echo ""
echo "✅ Done! Your project is ready."
echo ""
echo "📁 Folder : $(pwd)"
echo "🔗 GitHub : https://github.com/$GITHUB_USER/$PROJECT"
echo ""
echo "Next steps:"
echo "  1. Paste your PROJECT.md and code files from Cowork into this folder"
echo "  2. Run the app:"
echo "     source venv/bin/activate && python3 -m uvicorn main:app --reload"
echo "  3. Open http://localhost:8000"
echo ""
`;
}

function downloadSetupScript(ideaText) {
  const script = buildSetupScript(ideaText);
  const blob = new Blob([script], { type: 'text/x-sh' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  const projectName = slugify(ideaText) || 'my-app';
  a.href = url;
  a.download = `setup-${projectName}.sh`;
  a.click();
  URL.revokeObjectURL(url);
}

async function loadIdeas() {
  try {
    const res = await fetch('/ideas');
    const ideas = await res.json();
    ideasList.innerHTML = '';
    if (ideas.length === 0) {
      ideasList.innerHTML = '<p style="color:var(--muted);font-size:0.9rem;">No ideas yet. Type one above.</p>';
      return;
    }
    ideas.forEach(idea => {
      const card = document.createElement('div');
      card.className = 'idea-card';
      card.innerHTML = `
        <div class="idea-text">${idea.text}</div>
        <div class="idea-meta">${formatDate(idea.created_at)}</div>
        <div class="card-actions">
          <button class="expand-btn" data-id="${idea.id}" data-text="${encodeURIComponent(idea.text)}">
            Expand in Cowork
          </button>
          <button class="setup-btn" data-text="${encodeURIComponent(idea.text)}">
            ↓ setup.sh
          </button>
        </div>
      `;
      ideasList.appendChild(card);
    });

    document.querySelectorAll('.expand-btn').forEach(btn => {
      btn.addEventListener('click', () => {
        const text = decodeURIComponent(btn.dataset.text);
        const prompt = buildCoworkPrompt(text);
        navigator.clipboard.writeText(prompt).then(() => {
          btn.textContent = 'Copied!';
          showToast('Prompt copied — paste into a new Cowork task');
          setTimeout(() => { btn.textContent = 'Expand in Cowork'; }, 3000);
        });
      });
    });

    document.querySelectorAll('.setup-btn').forEach(btn => {
      btn.addEventListener('click', () => {
        const text = decodeURIComponent(btn.dataset.text);
        downloadSetupScript(text);
        showToast('setup.sh downloaded — run: bash setup.sh');
      });
    });

  } catch (err) {
    console.error('Failed to load ideas:', err);
  }
}

saveBtn.addEventListener('click', async () => {
  const text = ideaInput.value.trim();
  if (!text) return;
  saveBtn.disabled = true;
  try {
    const res = await fetch('/idea', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ text })
    });
    if (!res.ok) throw new Error('Save failed');
    ideaInput.value = '';
    showToast('Idea saved ✓');
    await loadIdeas();
  } catch (err) {
    showToast('Error saving idea');
    console.error(err);
  } finally {
    saveBtn.disabled = false;
  }
});

ideaInput.addEventListener('keydown', (e) => {
  if (e.key === 'Enter') saveBtn.click();
});

loadIdeas();
